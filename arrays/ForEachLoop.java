class ForEachLoop {
    public static void main(String[] args) {
        int[] arr = { 35, 41, 18, 78, 2, 97, 21, 31, 42, 6 };

        for (int i : arr) {
            System.out.println(i);
        }

        String[] stud = { "Farah", "Falak", "Madiha", "Naveed", "Saif" };

        for (String i : stud) {
            System.out.println(i);
        }
    }
}

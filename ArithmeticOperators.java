class ArithmeticOperators {
    public static void main(String[] args) {
        int x = 100;
        int y = 50;

        System.out.println(x + y + 30);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

    }
}
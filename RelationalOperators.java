class RelationalOperators {
    public static void main(String[] args) {
        int a = 300;
        int b = 300;

        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(b < a);
        System.out.println(b > a);
        System.out.println("____________");

        System.out.println(a >= b);
        System.out.println(b >= a);
        System.out.println("____________");

        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
